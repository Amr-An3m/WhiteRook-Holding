class LanguageManager {
  constructor(translationsData) {
    this.translations = translationsData;
    this.currentLanguage = localStorage.getItem('language') || 'ar';
    this.init();
  }

  init() {
    this.applyLanguage(this.currentLanguage);
    this.setupListeners();
    this.initAnimations();
    this.initMobileMenu();
  }

  applyLanguage(lang) {
    this.currentLanguage = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    localStorage.setItem('language', lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.getTranslation(lang, key);
      if (text) {
        if (el.tagName === 'INPUT' && el.type === 'button') {
          el.value = text;
        } else {
          el.textContent = text;
        }
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const text = this.getTranslation(lang, key);
      if (text) {
        el.setAttribute('placeholder', text);
      }
    });

    if (window.lucide) {
      window.lucide.createIcons();
    }
    this.updateToggleButtons();
  }

  getTranslation(lang, keyPath) {
    const keys = keyPath.split('.');
    let result = this.translations[lang];
    for (const k of keys) {
      if (result !== undefined && result !== null) {
        result = result[k];
      } else {
        return undefined;
      }
    }
    return typeof result === 'string' ? result : undefined;
  }

  toggleLanguage() {
    const newLang = this.currentLanguage === 'en' ? 'ar' : 'en';
    this.applyLanguage(newLang);
  }

  updateToggleButtons() {
    const self = this;
    document.querySelectorAll('.lang-btn').forEach(btn => {
      const label = self.currentLanguage === 'en' ? 'AR' : 'EN';
      btn.innerHTML = "<i data-lucide='languages'></i> " + label;
    });
    if (window.lucide) {
      window.lucide.createIcons();
    }
  }

  setupListeners() {
    const self = this;
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => self.toggleLanguage());
    });
  }

  initAnimations() {
    const observerOptions = {
      threshold: 0.1,
      rootMargin: "0px 0px -50px 0px"
    };

    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
          observer.unobserve(entry.target);
        }
      });
    }, observerOptions);

    document.querySelectorAll('.fade-in').forEach(element => {
      observer.observe(element);
    });
  }

  initMobileMenu() {
    const mobileMenuBtn = document.getElementById('mobile-menu-btn');
    const mobileMenu = document.getElementById('mobile-menu');
    if (mobileMenuBtn && mobileMenu) {
      mobileMenuBtn.addEventListener('click', () => {
        mobileMenu.classList.toggle('hidden');
      });
    }
  }
}

document.addEventListener('DOMContentLoaded', () => {
  window.languageManager = new LanguageManager(translations);
});
